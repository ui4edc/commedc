package cn.com.ecrf.trq.model.stat;

public class ADEStat {
	private int id;
	private String no;
	private int age;
	private String sex;
	private String ethic;
	private String diagnosis;
	private String ade;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getEthic() {
		return ethic;
	}
	public void setEthic(String ethic) {
		this.ethic = ethic;
	}
	public String getDiagnosis() {
		return diagnosis;
	}
	public void setDiagnosis(String diagnosis) {
		this.diagnosis = diagnosis;
	}
	public String getAde() {
		return ade;
	}
	public void setAde(String ade) {
		this.ade = ade;
	}
	
	
}
