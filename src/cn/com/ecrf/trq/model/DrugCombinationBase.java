package cn.com.ecrf.trq.model;

public class DrugCombinationBase {

	private String no;
	private int id;
	private int hasDrug;
	private String drug;
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getHasDrug() {
		return hasDrug;
	}
	public void setHasDrug(int hasDrug) {
		this.hasDrug = hasDrug;
	}
	public String getDrug() {
		return drug;
	}
	public void setDrug(String drug) {
		this.drug = drug;
	}
	
	
	
}
